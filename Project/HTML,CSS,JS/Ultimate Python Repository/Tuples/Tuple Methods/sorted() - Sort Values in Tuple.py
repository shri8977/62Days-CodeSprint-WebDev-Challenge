### sorted() - Sort Values in Tuple ###


## Initialize Tuple
tuple = (10, 2, 3, 34, 78, 190, 500)

# sorted(Tuple) - Sort tuple values from least to greatest
sorted(tuple)

# Print sorted tuple
print (sorted(tuple))

## -------------------------------------------------------------------

## Sort the tuple - order of alphabetical characters
tuple2 = ('p', 'r', 'o', 'g', 'r', 'a', 'm')

# sorted(Tuple) - Sort tuple in alphabetical order of characters
sorted(tuple2)

# Print sorted tuple
print (sorted(tuple2))
