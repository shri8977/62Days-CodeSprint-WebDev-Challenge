### count() - Count Values in Tuple ###

# Initialize Tuple
tuple = ["carrot", "potato", "potato", "lettuce", "beans", "corn", "capsicum"]

# Tuple.count(Tuple) -- Count values in the tuple
print (tuple.count(tuple))

# Tuple.count(Value) -- Counts no. of times value appears in tuple
print (tuple.count("potato"))
print (tuple.count("corn"))
