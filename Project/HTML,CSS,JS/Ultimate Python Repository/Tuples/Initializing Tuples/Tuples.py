### Initializing Tuples ###

  # Tuples are used to store multiple elements of different data types in a single variable.
  # Ex: tuple = (2, "element")
  
  # Tuples are ordered and unchangeable, unlike lists which are changeable.

  # Tuple packing: creating a tuple without parentheses (), ex: 1, 2, 3
  
  # The elements in a tuple cannot be reversed.


# Initialize Tuple
  # Tuple = (No. of items)
tuple = (10)

# Print tuple
print(tuple)

## Print a set of elements as a tuple
tuple2 = 1, 2, "element"
print (tuple2)

# Print data type of a tuple
tuple3 = ("hello")
print (type(tuple3))

# Print element in tuple with specific index
tuple4 = (1, 2, 3, 4, 5, "AA", 6)

# Print element at specific index
tuple4[4]
print (tuple4[4])

tuple4[-3]
print (tuple4[-3])

tuple4[2:4]
print (tuple4[2:4])
